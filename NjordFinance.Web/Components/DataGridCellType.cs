namespace NjordFinance.Web.Components
{
    /// <summary>
    /// Enumerate the supported cell types.
    /// </summary>
    public enum DataGridCellType
    {
        /// <summary>
        /// The cell contains a navigation link.
        /// </summary>
        Link,

        /// <summary>
        /// The cell contains raw text.
        /// </summary>
        Text,

        /// <summary>
        /// The cell contains a user control.
        /// </summary>
        Control,

        /// <summary>
        /// The cell contains a button.
        /// </summary>
        Button
    }
}
